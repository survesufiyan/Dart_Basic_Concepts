void main() {
  int number = 84;
  if (number % 2 == 0) {
    print("Even Number");
  } else if (number % 3 == 0) {
    print("Odd Number");
  }
  print("End of Program");

  int marks = 56;
  if (marks >= 90) {
    print("Grade A");
  } else if (marks >= 80) {
    print("Grade B");
  } else if (marks >= 70) {
    print("Grade C");
  } else if (marks >= 60) {
    print("Grade D");
  } else {
    print("Grade F");
  }
  print("End of Program");
}
