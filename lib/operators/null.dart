void main() {
  // Null-aware operators
  // ??, ??=, !, ?.
  // ?? operator: Returns the right-hand operand if the left-hand operand is null, otherwise returns the left-hand operand.
  // var keyword is used to declare a variable without specifying its type.
  var a;
  var b;

  print(a); // null
  print(b); // null
}
